package it.polito.tdp.nyc.model;

public class Distanza {
	private String q;
	private double dis;
	
	public Distanza(String q, double dis) {
		super();
		this.q = q;
		this.dis = dis;
	}

	public String getQ() {
		return q;
	}

	public void setQ(String q) {
		this.q = q;
	}

	public double getDis() {
		return dis;
	}

	public void setDis(double dis) {
		this.dis = dis;
	}

	
	
}
